package cm.bdqn.packet.domain;

public class BookQry {
    private String id;
    private String name; // 书名

    public BookQry(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
